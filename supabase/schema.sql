create table if not exists orders (
  id bigint generated by default as identity primary key,
  "customerName" text not null,
  phone text not null,
  address text not null,
  notes text,
  items jsonb not null,
  subtotal numeric not null default 0,
  deposit numeric not null default 0,
  fee numeric not null default 0,
  "paymentMethod" text not null,
  "paymentNumber" text not null,
  "receiptImage" text,
  status text not null,
  "deliveryDate" text not null,
  "deliveryFee" numeric,
  "createdAt" timestamptz not null default now(),
  "updatedAt" timestamptz not null default now()
);

create table if not exists settings (
  key text primary key,
  value text not null,
  "updatedAt" timestamptz not null default now()
);

create table if not exists password_audit (
  id bigint generated by default as identity primary key,
  "changedAt" timestamptz not null default now(),
  note text not null
);

alter table products enable row level security;
alter table orders enable row level security;
alter table settings enable row level security;
alter table password_audit enable row level security;

revoke all on products from anon, authenticated;
revoke all on orders from anon, authenticated;
revoke all on settings from anon, authenticated;
revoke all on password_audit from anon, authenticated;


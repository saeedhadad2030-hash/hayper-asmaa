create table if not exists products (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null,
  price numeric not null default 0,
  "originalPrice" numeric,
  "offerActive" boolean not null default false,
  "variablePrice" boolean not null default false,
  available boolean not null default true,
  image text,
  "createdAt" timestamptz not null default now(),
  "updatedAt" timestamptz not null default now()
);

alter table products add column if not exists "originalPrice" numeric;
alter table products add column if not exists "offerActive" boolean not null default false;
alter table products add column if not exists stock integer default null;

create table if not exists categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  "sortOrder" integer not null default 0
);


export function getDb() {
  if (db) return db;

  db = new DatabaseSync(dbPath);
  db.exec(`
    PRAGMA journal_mode = WAL;
    CREATE TABLE IF NOT EXISTS products (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL,
      category TEXT NOT NULL,
      price REAL NOT NULL,
      originalPrice REAL,
      offerActive INTEGER NOT NULL DEFAULT 0,
      variablePrice INTEGER NOT NULL DEFAULT 0,
      available INTEGER NOT NULL DEFAULT 1,
      stock INTEGER DEFAULT NULL,
      image TEXT,
      createdAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
    );
    CREATE TABLE IF NOT EXISTS categories (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL UNIQUE,
      sortOrder INTEGER NOT NULL DEFAULT 0
    );
    CREATE TABLE IF NOT EXISTS orders (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      customerName TEXT NOT NULL,
      phone TEXT NOT NULL,
      address TEXT NOT NULL,
      notes TEXT,
      items TEXT NOT NULL,
      subtotal REAL NOT NULL,
      deposit REAL NOT NULL,
      fee REAL NOT NULL,
      paymentMethod TEXT NOT NULL,
      paymentNumber TEXT NOT NULL,
      receiptImage TEXT,
      status TEXT NOT NULL,
      deliveryDate TEXT NOT NULL,
      deliveryFee REAL,
      createdAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
    );
    CREATE TABLE IF NOT EXISTS settings (
      key TEXT PRIMARY KEY,
      value TEXT NOT NULL,
      updatedAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
    );
    CREATE TABLE IF NOT EXISTS password_audit (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      changedAt TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
      note TEXT NOT NULL
    );
  `);

  const productColumns = db.prepare("PRAGMA table_info(products)").all().map((column) => column.name);
  if (!productColumns.includes("originalPrice")) {
    db.exec("ALTER TABLE products ADD COLUMN originalPrice REAL");
  }
  if (!productColumns.includes("offerActive")) {
    db.exec("ALTER TABLE products ADD COLUMN offerActive INTEGER NOT NULL DEFAULT 0");
  }
  if (!productColumns.includes("stock")) {
    db.exec("ALTER TABLE products ADD COLUMN stock INTEGER DEFAULT NULL");
  }

  // Seed default categories with sort order (تورت last)
  const defaultCategories = [
    ["حلويات شريف الزيني", 1],
    ["مخبوزات", 2],
    ["مشكل حلويات", 3],
    ["تورت", 99]
  ];
  const insertCat = db.prepare("INSERT OR IGNORE INTO categories (name, sortOrder) VALUES (?, ?)");
  for (const [catName, catOrder] of defaultCategories) {
    insertCat.run(catName, catOrder);
  }

  const insert = db.prepare(`
    INSERT INTO products (name, category, price, variablePrice, available)
    VALUES (?, ?, ?, ?, 1)
  `);
  const exists = db.prepare("SELECT COUNT(*) AS count FROM products WHERE name = ? AND category = ?");
  for (const [name, category, price] of [...products, ...cakeProducts]) {
    if (exists.get(name, category).count === 0) {
      insert.run(name, category, price, 0);
    }
  }

  return db;
}

// node:sqlite returns objects with null prototype; spread into plain objects
// so they can be serialized from Server Components to Client Components.
export function rows(statement, ...params) {
  return getDb().prepare(statement).all(...params).map(r => ({ ...r }));
}

export function row(statement, ...params) {
  const r = getDb().prepare(statement).get(...params);
  return r ? { ...r } : r;
}

export function run(statement, ...params) {
  return getDb().prepare(statement).run(...params);
}
